#import "LSPhoneInfoObject.h"
#import "LiveModule.h"

@implementation LSPhoneInfoObject


- (LSPhoneInfoObject *)initWithAction:(int)actionType
{
    self = [super init];
    if (nil != self)
    {
        self.model = [LSPhoneInfoObject iphoneType];
        self.manufacturer = @"APPLE";
        self.osType = [[UIDevice currentDevice] systemName] == nil?
        @"":[[UIDevice currentDevice] systemName];
        self.releaseVer = [[UIDevice currentDevice] systemVersion] == nil?
        @"":[[UIDevice currentDevice] systemVersion];
        self.sdk = @"";
        self.density = 0.0;
        self.densityDpi = @"";
        self.width = screenSize.width;
        self.height = screenSize.height;
        self.manId = [LSLoginManager manager].loginItem.userId;
        self.verCode = [[LiveModule module].appVerCode intValue];
        self.verName = [LiveModule module].appStringVerCode;
        self.phoneType = 1;
        self.networkType = 0;
        self.language =  [[NSLocale currentLocale] objectForKey:NSLocaleLanguageCode];
        self.country = [[NSLocale currentLocale] objectForKey:NSLocaleCountryCode];
        self.siteId = HTTP_OTHER_SITE_LIVE;
        self.action = actionType;
        self.lineNumber = @"";
        self.deviceId = [UIDevice currentDevice].identifierForVendor.UUIDString == nil?
        @"":[UIDevice currentDevice].identifierForVendor.UUIDString;
        CTTelephonyNetworkInfo *info = [[CTTelephonyNetworkInfo alloc] init];
        CTCarrier *carrier = info.subscriberCellularProvider;
        self.simOptName = carrier.carrierName == nil?
        @"":carrier.carrierName;
        self.simOpt = carrier.mobileCountryCode == nil?@"":carrier.mobileCountryCode;
        self.simCountryIso = carrier.isoCountryCode == nil?@"":carrier.isoCountryCode;
        self.simState = @"";
    }
    return self;
}

